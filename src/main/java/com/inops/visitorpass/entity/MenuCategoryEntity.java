package com.inops.visitorpass.entity;

import java.io.Serializable;
import java.util.ArrayList;
import java.util.List;

import javax.persistence.CascadeType;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.FetchType;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.OneToMany;
import javax.persistence.Table;

import lombok.AllArgsConstructor;
import lombok.Getter;
import lombok.NoArgsConstructor;
import lombok.Setter;
import lombok.experimental.Accessors;

@Getter
@Setter
@Accessors(chain = true)
@NoArgsConstructor
@AllArgsConstructor
@Entity
@Table(name = "MenuCategory")
public class MenuCategoryEntity implements Serializable {
	
	@Id
	@Column(name="Menu_Category_Id",nullable=false)
	@GeneratedValue(strategy = GenerationType.IDENTITY)
	private Long menuCategoryId;

    private String label;  
    private boolean custom;
   
    
    @OneToMany(cascade = CascadeType.ALL, fetch = FetchType.EAGER, orphanRemoval = true)
    @JoinColumn(name = "menuCategoryId")
    private List<MenuItemEntity> menuItem = new ArrayList<>();

   
}
